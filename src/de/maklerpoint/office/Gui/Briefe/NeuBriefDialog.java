    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        if (this.update == false) {
            int dial = JOptionPane.NO_OPTION;
            dial = JOptionPane.showConfirmDialog(null, "Wollen Sie das Fenster wirklich schließen? Alle ihre Eingaben "
                    + "gehen in diesem Fall verloren.", "Wollen Sie das Fenster schließen?", JOptionPane.YES_NO_OPTION);
            if (dial == JOptionPane.YES_OPTION) {
                this.dispose();
            }
        } else {
            this.dispose();
        }
}//GEN-LAST:event_btnCancelActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (update == false) {
            brief = new BriefObj();
        }

        brief.setName(field_name.getText());
        brief.setFullpath(field_file.getText());

        BriefCategoryObj bcat = (BriefCategoryObj) this.combo_category.getSelectedItem();

        brief.setCategoryId(bcat.getId());

        brief.setTag(this.combo_tag.getSelectedItem().toString());
        brief.setType(this.combo_type.getSelectedIndex());

        brief.setBenutzer(check_benutzer.isSelected());
        brief.setGeschKunde(check_gesch.isSelected());
        brief.setPrivatKunde(check_privat.isSelected());
        brief.setProdukt(check_produkt.isSelected());
        brief.setStoerfall(check_stoer.isSelected());
        brief.setVersicherer(check_vers.isSelected());
        brief.setVertrag(check_vertrag.isSelected());

        brief.setBeschreibung(text_beschreibung.getText());
        brief.setComments(text_commments.getText());

        try {
            if (update) {
                BriefeSQLMethods.updateBriefe(DatabaseConnection.open(), brief);
            } else {
                brief.setCreated(new java.sql.Timestamp(System.currentTimeMillis()));
                BriefeSQLMethods.insertIntoBriefe(DatabaseConnection.open(), brief);
            }
        } catch (Exception e) {
            Log.databaselogger.fatal("Datenbankfehler: Konnte Brief nicht speichern", e);
            ShowException.showException("Bei der Speicherung des Briefes ist ein Datenbank Fehler aufgetretten. "
                    + "Sollte dieser häufiger auftretten wenden Sie sich bitte an den Support.",
                    ExceptionDialogGui.LEVEL_WARNING, e,
                    "Schwerwiegend: Konnte Brief nicht speichern");
        }

        this.dispose();
    }//GEN-LAST:event_btnSaveActionPerformed

