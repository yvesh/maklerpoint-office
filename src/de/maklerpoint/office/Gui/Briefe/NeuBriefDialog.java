    private void setUp() {
        this.setTitle("Neuer Brief / Fax / E-Mail");
        this.combo_type.setEnabled(true);
        this.combo_tag.setModel(new DefaultComboBoxModel(Tags.tags));
        this.combo_category.setModel(new DefaultComboBoxModel(ComboBoxGetter.getBriefCategoriesCombo(null)));
        this.combo_type.setModel(new DefaultComboBoxModel(Briefe.TYPES));
        loadBrief();
    }

    private void loadBrief() {
        if (brief == null) {
            return;
        }

        this.field_name.setText(brief.getName());
        this.field_file.setText(brief.getFullpath());
        this.combo_category.setSelectedItem(BriefeHelper.getBriefCategoryObj(brief.getCategoryId()));
        this.combo_tag.setSelectedItem(Tags.getTag(brief.getTag()));
        this.combo_type.setSelectedIndex(brief.getType());
        this.combo_type.setEnabled(false);

        this.label_checksum.setText(brief.getChecksum());
        this.label_created.setText(df.format(brief.getCreated()));
        this.label_modified.setText(df.format(brief.getModified()));
        this.label_creator.setText(BenutzerRegistry.getBenutzer(brief.getCreatorId(), true).toString());

        this.check_benutzer.setSelected(brief.isBenutzer());
        this.check_gesch.setSelected(brief.isGeschKunde());
        this.check_privat.setSelected(brief.isPrivatKunde());
        this.check_produkt.setSelected(brief.isProdukt());
        this.check_stoer.setSelected(brief.isStoerfall());
        this.check_vers.setSelected(brief.isVersicherer());
        this.check_vertrag.setSelected(brief.isBenutzer());

        this.text_beschreibung.setText(brief.getBeschreibung());
        this.text_commments.setText(brief.getComments());
        this.setTitle(brief.getName() + " (" + brief.getFullpath() + ")");
    }

